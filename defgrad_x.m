function [F] = defgrad_x(u_elem,dshape)
%Bestimmung des Deformationsgradienten F aus grad(u) in R_x

% F^(-1) = dX/dx     = grad(X)
%        = d(x-u)/dx
%        = I - du/dx = I - grad(u)

dim = size(u_elem);
dim = dim(2);             % 2- oder 3-D ?

u_x = u_elem' * dshape;
F = inv(-u_x + eye(dim));

