function CB_f2f_apply

global fid_f2f;
global handles;
global ls;
global X;

% Doppelklick
i = get(ls,'Value');
X = i;
filename = handles.file_names(i);
filename = filename{1,1};
 
cd parser

sys = computer;
if isunix
    unix(['perl f2f.pl ',filename]);
end

if strncmp(sys, 'PC', 2)
    f2f = perl('f2f.pl ',filename)
end

cd ..